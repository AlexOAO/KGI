def main():
    conn = get_conn()
    try:
        with conn.cursor() as cur:
            # 1. 確認 source_document 欄位是否存在
            cur.execute("SHOW COLUMNS FROM modules LIKE 'source_document'")
            if not cur.fetchone():
                cur.execute("ALTER TABLE modules ADD COLUMN source_document VARCHAR(512) DEFAULT NULL")
                print("Added source_document column to modules")

            # 2. 刪除所有 12 個模組的舊閃卡與題目（先清子表）
            cur.execute("SET FOREIGN_KEY_CHECKS=0")
            cur.execute("DELETE FROM flashcard_pages WHERE module_id BETWEEN 1 AND 12")
            cur.execute("DELETE FROM question_responses WHERE question_id IN (SELECT id FROM questions WHERE module_id BETWEEN 1 AND 12)")
            cur.execute("DELETE FROM questions WHERE module_id BETWEEN 1 AND 12")
            cur.execute("SET FOREIGN_KEY_CHECKS=1")
            print("Cleared old flashcards and questions for modules 1-12")

            # 3. 重新插入真實條文內容
            for mid in range(1, 13):
                cards = FLASHCARDS.get(mid, [])
                for seq, text in enumerate(cards, start=1):
                    cur.execute(
                        "INSERT INTO flashcard_pages (module_id, sequence_number, page_text, image_url) VALUES (%s,%s,%s,%s)",
                        (mid, seq, text, None)
                    )

                qs = QUESTIONS.get(mid, [])
                for qtype, prompt, opts, ans in qs:
                    cur.execute(
                        "INSERT INTO questions (module_id, type, prompt, options_json, correct_answer) VALUES (%s,%s,%s,%s,%s)",
                        (mid, qtype, prompt, opts, ans)
                    )

                # 4. 更新 source_document
                cur.execute(
                    "UPDATE modules SET source_document=%s WHERE id=%s",
                    (MODULE_SOURCES[mid], mid)
                )
                print(f"  Module {mid}: {len(cards)} cards, {len(qs)} questions → {MODULE_SOURCES[mid]}")

        conn.commit()
        print("\nDone. All 12 modules rebuilt from real law data.")
    finally:
        conn.close()


if __name__ == "__main__":
    main()
