def main():
    conn = get_conn()
    try:
        with conn.cursor() as cur:
            total_added = 0
            for mid, cards in EXTRA_CARDS.items():
                for offset, text in enumerate(cards):
                    seq = 6 + offset  # 序號 6 至 10
                    cur.execute(
                        "INSERT IGNORE INTO flashcard_pages (module_id, sequence_number, page_text, image_url) "
                        "VALUES (%s, %s, %s, NULL)",
                        (mid, seq, text),
                    )
                    total_added += cur.rowcount
                print(f"  Module {mid}: +{len(cards)} cards (seq 6-10), pool now 10")
        conn.commit()
        print(f"\nDone. Added {total_added} extra flashcards. Each module now has 10 articles in the pool.")
    finally:
        conn.close()


if __name__ == "__main__":
    main()
